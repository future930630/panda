def to_js_obj(p):
    lines = ["  {"]
    for k, v in p.items():
        indent = "    "
        if isinstance(v, list):
            if not v:
                lines.append(f"{indent}{json.dumps(k)}: [],")
            elif all(isinstance(x, str) for x in v):
                lines.append(f"{indent}{json.dumps(k)}: {json.dumps(v)},")
            else:
                arr_lines = [f"{indent}    {json.dumps(x)}" for x in v]
                lines.append(f"{indent}{json.dumps(k)}: [\n{','.join(arr_lines)}\n{indent}  ],")
        elif isinstance(v, dict):
            kv = ", ".join(f"{json.dumps(key)}: {json.dumps(val)}" for key, val in v.items())
            lines.append(f"{indent}{json.dumps(k)}: {{{kv}}},")
        else:
            lines.append(f"{indent}{json.dumps(k)}: {json.dumps(v)},")
    lines.append("  },")
    return "\n".join(lines)

